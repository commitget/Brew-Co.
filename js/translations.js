function setLang(locale) {
  var t = lang[locale];
  if (!t) return;
  document.getElementById('htmlTag').lang = locale;
  document.getElementById('langToggle').textContent = locale === 'ru' ? 'EN' : 'RU';
  document.querySelectorAll('[data-i18n]').forEach(function (el) {
    var key = el.dataset.i18n;
    if (t[key] !== undefined) el.innerHTML = t[key];
  });
  try { localStorage.setItem('brew-lang', locale); } catch(e) {}
}
